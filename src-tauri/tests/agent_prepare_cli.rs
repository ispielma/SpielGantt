#[test]
fn agent_prepare_json_creates_project_agent_scaffolding() {
    let workspace_dir = tempdir().expect("temporary directory should be created");
    let project_dir = support::create_unprepared_project(workspace_dir.path(), "project");
    let canonical_project_dir =
        std::fs::canonicalize(&project_dir).expect("project directory should canonicalize");

    let prepare_output = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "prepare", "--json", "project"],
    );
    assert!(
        prepare_output.status.success(),
        "agent prepare --json should succeed: {prepare_output:?}"
    );
    assert!(
        support::stderr_text(&prepare_output).is_empty(),
        "agent prepare --json should keep diagnostics out of stdout JSON"
    );

    let prepared: Value = serde_json::from_str(&support::stdout_text(&prepare_output))
        .expect("agent prepare --json stdout should be valid JSON");
    assert_eq!(prepared["schema_version"], 1);
    assert_eq!(
        prepared["project_root"],
        canonical_project_dir.display().to_string()
    );
    assert_eq!(prepared["outcome"], "created");
    assert_eq!(prepared["agent"]["ready"], true);
    assert_eq!(
        prepared["agent"]["recorded_cli_path"],
        env!("CARGO_BIN_EXE_spielgantt")
    );

    assert!(
        project_dir.join("AGENTS.md").is_file(),
        "agent prepare should write project AGENTS.md"
    );
    let project_guidance = std::fs::read_to_string(project_dir.join("AGENTS.md"))
        .expect("generated AGENTS.md should be readable");
    assert!(
        project_guidance.contains("Generated by SpielGantt agent scaffolding"),
        "generated AGENTS.md should carry the managed-file marker"
    );
    for skill_name in [
        "review-spielgantt",
        "setup-spielgantt",
        "update-spielgantt",
        "use-spielgantt",
    ] {
        assert!(
            project_dir
                .join(".agents/skills")
                .join(skill_name)
                .join("SKILL.md")
                .is_file(),
            "agent prepare should write the {skill_name} project-local skill"
        );
        let skill = std::fs::read_to_string(
            project_dir
                .join(".agents/skills")
                .join(skill_name)
                .join("SKILL.md"),
        )
        .expect("generated project-local skill should be readable");
        assert!(
            skill.contains("Generated by SpielGantt agent scaffolding"),
            "generated {skill_name} skill should carry the managed-file marker"
        );
    }

    let metadata = std::fs::read_to_string(project_dir.join(".spielgantt/agent.json"))
        .expect("agent prepare should write local agent metadata");
    let metadata: serde_json::Value =
        serde_json::from_str(&metadata).expect("agent metadata should be valid JSON");
    assert_eq!(metadata["schema_version"], 1);
    assert_eq!(metadata["cli_path"], env!("CARGO_BIN_EXE_spielgantt"));
    assert_eq!(metadata["version"], env!("CARGO_PKG_VERSION"));

    let status_output = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "status", "--json", "project"],
    );
    assert!(
        status_output.status.success(),
        "agent status --json should succeed after preparing scaffolding: {status_output:?}"
    );
    let status: Value = serde_json::from_str(&support::stdout_text(&status_output))
        .expect("agent status --json stdout should be valid JSON");
    assert_eq!(status["schema_version"], 1);
    assert_eq!(status["agent"], prepared["agent"]);
}

#[test]
fn agent_prepare_generates_use_skill_as_the_concrete_cli_reference() {
    let workspace_dir = tempdir().expect("temporary directory should be created");
    let project_dir = support::create_unprepared_project(workspace_dir.path(), "project");

    let prepare_output = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "prepare", "--json", "project"],
    );
    assert!(
        prepare_output.status.success(),
        "agent prepare --json should succeed: {prepare_output:?}"
    );

    let use_skill =
        std::fs::read_to_string(project_dir.join(".agents/skills/use-spielgantt/SKILL.md"))
            .expect("generated use skill should be readable");
    for expected_command in [
        "`spielgantt agent snapshot --json [PATH]`",
        "`spielgantt agent prepare --json [PATH]`",
        "`spielgantt agent status --json [PATH]`",
        "`spielgantt validate [--json] [PATH]`",
        "`spielgantt repair [PATH]`",
        "`spielgantt export [PATH]`",
        "`spielgantt task adoptable-folders --json`",
        "`spielgantt task create <TASK_NAME>`",
        "`spielgantt task insert-before <SELECTED_TASK_NAME> <TASK_NAME> --json`",
        "`spielgantt task insert-after <SELECTED_TASK_NAME> <TASK_NAME> --json`",
        "`spielgantt task delete <TASK_NAME> [--remove-from-chart|--delete-directory] [--json]`",
        "`spielgantt task update <TASK_NAME> --status <blocked|unblocked|done>`",
        "`spielgantt task dependency remove <TASK_NAME> <BLOCKER_NAME>`",
        "`spielgantt task relationships --json`",
        "`spielgantt task workflow --json`",
        "`spielgantt event create <EVENT_NAME>`",
        "`spielgantt project update-readme --content <CONTENT> --expected-version <README_VERSION> --json`",
    ] {
        assert!(
            use_skill.contains(expected_command),
            "generated use skill should document {expected_command}: {use_skill}"
        );
    }

    for stale_reference in [
        ".spielgantt/agent.yaml",
        "spielgantt task link",
        "--start-date",
        "--end-date",
        "--progress",
        "task links",
    ] {
        assert!(
            !use_skill.contains(stale_reference),
            "generated use skill should not document stale CLI reference {stale_reference:?}: {use_skill}"
        );
    }

    assert!(
        use_skill.contains(".spielgantt/agent.json"),
        "generated use skill should document current agent metadata path: {use_skill}"
    );
}

