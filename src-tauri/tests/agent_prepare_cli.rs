#[test]
fn agent_prepare_refreshes_managed_files_without_removing_user_files() {
    let workspace_dir = tempdir().expect("temporary directory should be created");
    let project_dir = support::create_unprepared_project(workspace_dir.path(), "project");

    let first_prepare = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "prepare", "--json", "project"],
    );
    assert!(
        first_prepare.status.success(),
        "initial agent prepare should succeed: {first_prepare:?}"
    );

    std::fs::write(project_dir.join("notes.txt"), "ordinary project notes")
        .expect("ordinary user file should be created");
    std::fs::create_dir_all(project_dir.join(".agents/skills/custom-skill"))
        .expect("extra user skill directory should be created");
    std::fs::write(
        project_dir.join(".agents/skills/custom-skill/SKILL.md"),
        "user-owned skill",
    )
    .expect("extra user skill should be written");
    let managed_agents = std::fs::read_to_string(project_dir.join("AGENTS.md"))
        .expect("managed guidance should be readable before refresh");
    std::fs::write(
        project_dir.join("AGENTS.md"),
        managed_agents.replace(
            "# SpielGantt Project Agent Instructions",
            "# Stale SpielGantt Project Agent Instructions",
        ),
    )
    .expect("managed guidance should be changed before refresh");

    let refresh_output = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "prepare", "--json", "project"],
    );
    assert!(
        refresh_output.status.success(),
        "agent prepare should refresh managed files: {refresh_output:?}"
    );
    let refresh: Value = serde_json::from_str(&support::stdout_text(&refresh_output))
        .expect("agent prepare refresh stdout should be valid JSON");
    assert_eq!(refresh["outcome"], "refreshed");

    let refreshed_agents = std::fs::read_to_string(project_dir.join("AGENTS.md"))
        .expect("refreshed AGENTS.md should be readable");
    assert!(
        refreshed_agents.contains("# SpielGantt Project Agent Instructions"),
        "agent prepare should refresh managed project guidance"
    );
    assert_eq!(
        std::fs::read_to_string(project_dir.join("notes.txt"))
            .expect("ordinary user file should remain readable"),
        "ordinary project notes"
    );
    assert_eq!(
        std::fs::read_to_string(project_dir.join(".agents/skills/custom-skill/SKILL.md"))
            .expect("extra user skill should remain readable"),
        "user-owned skill"
    );

    let unchanged_output = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "prepare", "--json", "project"],
    );
    assert!(
        unchanged_output.status.success(),
        "agent prepare should remain idempotent after refresh: {unchanged_output:?}"
    );
    let unchanged: Value = serde_json::from_str(&support::stdout_text(&unchanged_output))
        .expect("unchanged agent prepare stdout should be valid JSON");
    assert_eq!(unchanged["outcome"], "unchanged");
}

#[test]
fn agent_prepare_refuses_to_overwrite_preexisting_user_agents_file() {
    let workspace_dir = tempdir().expect("temporary directory should be created");
    let project_dir = support::create_unprepared_project(workspace_dir.path(), "project");
    std::fs::write(
        project_dir.join("AGENTS.md"),
        "user-owned project instructions",
    )
    .expect("preexisting user AGENTS.md should be written");

    let prepare_output = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "prepare", "--json", "project"],
    );
    assert!(
        !prepare_output.status.success(),
        "agent prepare should fail instead of overwriting user AGENTS.md: {prepare_output:?}"
    );
    assert!(
        support::stdout_text(&prepare_output).is_empty(),
        "failed JSON prepare should not emit a partial JSON report"
    );
    assert!(
        support::stderr_text(&prepare_output).contains("refusing to overwrite"),
        "agent prepare should explain the unmanaged file conflict: {prepare_output:?}"
    );
    assert_eq!(
        std::fs::read_to_string(project_dir.join("AGENTS.md"))
            .expect("preexisting user AGENTS.md should remain readable"),
        "user-owned project instructions"
    );
    assert!(
        !project_dir.join(".agents").exists(),
        "agent prepare should not partially create skills after an AGENTS.md conflict"
    );
    assert!(
        !project_dir.join(".spielgantt/agent.json").exists(),
        "agent prepare should not write metadata after an AGENTS.md conflict"
    );
}

#[test]
fn agent_prepare_refuses_to_overwrite_preexisting_same_named_user_skill() {
    let workspace_dir = tempdir().expect("temporary directory should be created");
    let project_dir = support::create_unprepared_project(workspace_dir.path(), "project");
    let user_skill = project_dir.join(".agents/skills/use-spielgantt/SKILL.md");
    std::fs::create_dir_all(
        user_skill
            .parent()
            .expect("skill path should have a parent"),
    )
    .expect("preexisting user skill directory should be created");
    std::fs::write(&user_skill, "user-owned use-spielgantt guidance")
        .expect("preexisting same-named user skill should be written");

    let prepare_output = support::run_spielgantt_in(
        workspace_dir.path(),
        &["agent", "prepare", "--json", "project"],
    );
    assert!(
        !prepare_output.status.success(),
        "agent prepare should fail instead of overwriting same-named user skill: {prepare_output:?}"
    );
    assert!(
        support::stderr_text(&prepare_output).contains("refusing to overwrite"),
        "agent prepare should explain the unmanaged skill conflict: {prepare_output:?}"
    );
    assert_eq!(
        std::fs::read_to_string(&user_skill)
            .expect("preexisting same-named user skill should remain readable"),
        "user-owned use-spielgantt guidance"
    );
    assert!(
        !project_dir.join("AGENTS.md").exists(),
        "agent prepare should not partially write AGENTS.md after a skill conflict"
    );
    assert!(
        !project_dir.join(".spielgantt/agent.json").exists(),
        "agent prepare should not write metadata after a skill conflict"
    );
}
